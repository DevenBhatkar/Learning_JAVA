public class JAVA_2_literals {
    public static void main(String[] args) {



                // Declaring and initializing a byte variable (Range: -128 to 127)
                byte age = 34;

                // Declaring and initializing an integer variable
                int age2 = 56;

                // Declaring and initializing a short variable (Range: -32,768 to 32,767)
                short age3 = 87;

                // Declaring and initializing a long variable (Use 'L' at the end to specify long type)
                long ageDino = 5666666666666L;

                // Declaring and initializing a character variable
                char ch = 'A';

                // Declaring and initializing a float variable (Use 'f' at the end to specify float type)
                float f1 = 5.6f;

                // Declaring and initializing a double variable (Default type for decimal values)
                double d1 = 4.66;

                // Declaring and initializing a boolean variable (Stores true or false)
                boolean a = true;

                // Printing the value of 'age' (34) without moving to the next line
                System.out.print(age);

                // Declaring and initializing a String variable
                String str = "deven";

                // Printing the value of 'str' ("deven") and moving to the next line
                System.out.println(str);
            }
        }



