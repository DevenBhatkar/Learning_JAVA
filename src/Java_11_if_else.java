public class Java_11_if_else {
    public static void main(String[] args) {

        // Declaring an integer variable 'age' and assigning it a value of 15
        int age = 15;

        // Using an if-else statement to check whether the person is eligible to drive
        if (age > 18) {  // Condition: If age is greater than 18
            System.out.println("You can drive");  // If true, this message is printed
        }
        else {  // If the age is not greater than 18
            System.out.println("You cannot drive");  // This message is printed instead
        }
    }
}
